<script>
    function check() {
        var cb = document.getElementsByTagName('input'),
            L = cb.length - 1,
            f = true;
        for (; L >= 0; L--) {
            if (cb[L]['type'] == 'checkbox' && cb[L]['checked'] == true) {
                f = !f;
                break;
            }
        }
        document.getElementById('delButton').disabled = f;
    }
</script>
